                            <hr>
                            <img src="assets/img/organisasi.png" width="80%">
                            <p>
                            <h3 class="text-uppercase">Pengarah Satu Data Pertanian</h3><br>Diemban: Sekretaris Jenderal Kementerian Pertanian</p>
                            <p>Pengarah Satu Data Pertanian adalah Pejabat Tinggi Madya yang ditunjuk untuk memberikan arahan dalam penyelenggaraan Satu Data Pertanian.</p>
                            <ul class="list-inline text-justify">Tugas:
                                <li>a. Mengoordinasikan dan menetapkan kebijakan terkait Satu Data Pertanian</li>
                                <li>b. Mengoordinasikan pelaksanaan Satu Data Pertanian; </li>
                                <li>c. Melakukan pemantauan dan evaluasi pelaksanaan Satu Data Pertanian; </li>
                                <li>d. Mengoordinasikan penyelesaian permasalahan dan hambatan pelaksanaan Satu Data Pertanian; dan</li>
                                <li>e. Menyampaikan laporan penyelenggaraan Satu Data Pertanian kepada Menteri.</li>
                            </ul>
                            <p>
                            <h3 class="text-uppercase">Wali Data</h3><br>Diemban : Pusat Data dan Sistem Informasi Pertanian</p>
                            <p>Walidata Pertanian adalah unit kerja Kementerian Pertanian yang ditunjuk untuk melaksanakan tugas sebagai Walidata di Kementerian Pertanian.</p>
                            <ul class="list-inline text-justify">Tugas:
                                <li>a. Mengumpulkan, memeriksa kesesuaian Data, dan mengelola Data yang disampaikan oleh Produsen Data Pertanian sesuai dengan prinsip Satu Data Indonesia;</li>
                                <li>b. Menyebarluaskan Data, Metadata, Kode Referensi, dan Data Induk di Portal Satu Data Indonesia dan Portal Satu Data Pertanian;</li>
                                <li>c. Membantu Pembina Data dalam membina Produsen Data Pertanian; dan</li>
                                <li>d. berkoordinasi dengan Produsen Data Pertanian untuk melakukan kegiatan pengumpulan data langsung di lapangan pada saat dibutuhkan dalam keadaan mendesak.</li>
                            </ul>
                            <p>
                            <h3 class="text-uppercase">Produsen Data</h3><br>Diemban : Seluruh Unit Kerja di Kementerian Pertanian</p>
                            <p>Produsen Data Pertanian adalah unit kerja di Kementerian Pertanian yang ditunjuk untuk melaksanakan tugas sebagai Produsen Data di Kementerian Pertanian.</p>
                            <ul class="list-inline text-justify">Tugas:
                                <li>a. Memberikan masukan kepada Pembina Data dan Menteri melalui Walidata Pertanian mengenai standar data, metadata dan interoperabilitas data</li>
                                <li>b. Menghasilkan Data sesuai dengan prinsip Satu Data Indonesia;</li>
                                <li>c. Menyampaikan Data dan Metadata kepada Walidata Pertanian; dan</li>
                                <li>d. Melakukan koordinasi dengan Sekretariat Direktorat Jenderal, Sekretariat Badan, atau Sekretariat Inspektorat Jenderal pembina unit kerja dalam pelaksanaan Satu Data Pertanian.</li>
                            </ul>

